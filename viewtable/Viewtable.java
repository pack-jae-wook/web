package viewtable;

public class Viewtable {
   private int tourismID;
   private String tourismTitle;
   private String userID;
   private String tourismDate;
   private String tourismContent;
   private int tourismAvailable;
   
   
   public int getTourismID() {
      return tourismID;
   }
   public void setTourismID(int tourismID) {
      this.tourismID = tourismID;
   }
   public String getTourismTitle() {
      return tourismTitle;
   }
   public void setTourismTitle(String tourismTitle) {
      this.tourismTitle = tourismTitle;
   }
   public String getUserID() {
      return userID;
   }
   public void setUserID(String userID) {
      this.userID = userID;
   }
   public String getTourismDate() {
      return tourismDate;
   }
   public void setTourismDate(String tourismDate) {
      this.tourismDate = tourismDate;
   }
   public String getTourismContent() {
      return tourismContent;
   }
   public void setTourismContent(String tourismContent) {
      this.tourismContent = tourismContent;
   }
   public int getTourismAvailable() {
      return tourismAvailable;
   }
   public void setTourismAvailable(int tourismAvailable) {
      this.tourismAvailable = tourismAvailable;
   }
   


}